module.exports={
    add : function(a,b){
        
        return parseFloat(a)+parseFloat(b);

    },

    subtract : function(a,b){

        return parseFloat(a)-parseFloat(b);
    },

    multiply : function(a,b){

        return parseFloat(a)*parseFloat(b);
    },

    divide : function(a,b){

        return parseFloat(a)/parseFloat(b);
    }

};
